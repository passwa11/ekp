package com.landray.kmss.km.imeeting.util;

import java.util.ArrayList;
import java.util.HashMap;
import java.util.List;
import java.util.Map;

/**
 * 统计结果集
 */
public class StatResult {

	// 题头相关信息
	private Map<String, Object> title = new HashMap<String, Object>();

	// X轴坐标信息
	private List<StatAxis> xAxis;

	// Y轴坐标信息
	private List<StatAxis> yAxis;

	// 统计data
	private List<Map<String, Object>> seriesData = new ArrayList<Map<String, Object>>();

	// 图表类型
	private String chartType;

	private String xAxisShowAll;

	private String xAxisRotate;

	public Map<String, Object> getTitle() {
		return title;
	}

	public void setTitle(Map<String, Object> title) {
		this.title = title;
	}

	public List<StatAxis> getxAxis() {
		return xAxis;
	}

	public void setxAxis(List<StatAxis> xAxis) {
		this.xAxis = xAxis;
	}

	public List<StatAxis> getyAxis() {
		return yAxis;
	}

	public void setyAxis(List<StatAxis> yAxis) {
		this.yAxis = yAxis;
	}

	public List<Map<String, Object>> getSeriesData() {
		return seriesData;
	}

	public void setSeriesData(List<Map<String, Object>> seriesData) {
		this.seriesData = seriesData;
	}

	public String getChartType() {
		return chartType;
	}

	public void setChartType(String chartType) {
		this.chartType = chartType;
	}

	public String getxAxisShowAll() {
		return xAxisShowAll;
	}

	public void setxAxisShowAll(String xAxisShowAll) {
		this.xAxisShowAll = xAxisShowAll;
	}

	public String getxAxisRotate() {
		return xAxisRotate;
	}

	public void setxAxisRotate(String xAxisRotate) {
		this.xAxisRotate = xAxisRotate;
	}

}
