package com.landray.kmss.km.forum.webservice;

import java.util.ArrayList;
import java.util.List;

import com.landray.kmss.sys.webservice2.forms.AttachmentForm;

public class KmForumPostParamterForm {
	// 版块名称
	private String fdForum = null;

	// 话题的发起者
	private String fdPoster = null;

	// 文档标题
	private String docSubject;

	// 文档内容
	private String docContent;

	// 附件
	private List<AttachmentForm> attachmentForms = new ArrayList<AttachmentForm>();

	public String getFdForum() {
		return fdForum;
	}

	public void setFdForum(String fdForum) {
		this.fdForum = fdForum;
	}

	public String getFdPoster() {
		return fdPoster;
	}

	public void setFdPoster(String fdPoster) {
		this.fdPoster = fdPoster;
	}

	public String getDocSubject() {
		return docSubject;
	}

	public void setDocSubject(String docSubject) {
		this.docSubject = docSubject;
	}

	public String getDocContent() {
		return docContent;
	}

	public void setDocContent(String docContent) {
		this.docContent = docContent;
	}

	public List<AttachmentForm> getAttachmentForms() {
		return attachmentForms;
	}

	public void setAttachmentForms(List<AttachmentForm> attachmentForms) {
		this.attachmentForms = attachmentForms;
	}

}
