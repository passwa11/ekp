package com.landray.kmss.common.dto;

/**
 * 设计详细信息<br>
 * 
 * fdContent是JSON字符串，保存的时候会自动从fdContent中抽取：label、messageKey、module的信息，作为摘要信息保存
 * 
 * @author 叶中奇
 */
public class DesignElementDetail {
	private String fdId;

	private String fdAppName;

	private String fdModule;

	private String fdMd5;

	private String fdContent;

	public String getFdId() {
		return fdId;
	}

	public void setFdId(String fdId) {
		this.fdId = fdId;
	}

	public String getFdAppName() {
		return fdAppName;
	}

	public void setFdAppName(String fdAppName) {
		this.fdAppName = fdAppName;
	}

	public String getFdModule() {
		return fdModule;
	}

	public void setFdModule(String fdModule) {
		this.fdModule = fdModule;
	}

	public String getFdMd5() {
		return fdMd5;
	}

	public void setFdMd5(String fdMd5) {
		this.fdMd5 = fdMd5;
	}

	public String getFdContent() {
		return fdContent;
	}

	public void setFdContent(String fdContent) {
		this.fdContent = fdContent;
	}
}
