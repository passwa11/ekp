package com.landray.kmss.sys.zone.context;

import java.util.ArrayList;
import java.util.List;

import com.landray.kmss.sys.attachment.model.SysAttMain;

/**
 * 图片上下文
 * 
 * @author XuJieYang
 * 
 */
public class ImageContext {
	private Integer startX;
	private Integer startY;
	private Integer width;
	private Integer height;
	private String zoomPath;

	private List<SysAttMain> attMains = new ArrayList<SysAttMain>();

	public ImageContext() {

	}

	public ImageContext(Integer startX, Integer startY, Integer width,
			Integer height, String zoomPath) {
		this.startX = startX;
		this.startY = startY;
		this.width = width;
		this.height = height;
		this.zoomPath = zoomPath;
	}

	public List<SysAttMain> getAttMains() {
		return attMains;
	}

	public void setAttMains(List<SysAttMain> attMains) {
		this.attMains = attMains;
	}

	public Integer getStartX() {
		return startX;
	}

	public void setStartX(Integer startX) {
		this.startX = startX;
	}

	public Integer getStartY() {
		return startY;
	}

	public void setStartY(Integer startY) {
		this.startY = startY;
	}

	public Integer getWidth() {
		return width;
	}

	public void setWidth(Integer width) {
		this.width = width;
	}

	public Integer getHeight() {
		return height;
	}

	public void setHeight(Integer height) {
		this.height = height;
	}
	public String getZoomPath() {
		return zoomPath;
	}

	public void setZoomPath(String zoomPath) {
		this.zoomPath = zoomPath;
	}
}
