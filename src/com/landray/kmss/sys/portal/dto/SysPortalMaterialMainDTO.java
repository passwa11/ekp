    public Double getFdLength() {
        return fdLength;
    }

    public void setFdLength(Double fdLength) {
        this.fdLength = fdLength;
    }

    public String getFdAttId() {
        return fdAttId;
    }

    public void setFdAttId(String fdAttId) {
        this.fdAttId = fdAttId;
    }

