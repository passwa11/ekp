package com.landray.kmss.sys.restservice.client;

import org.springframework.web.client.RestOperations;

/**
 * 
 * 如果{@link IRestClient}不满足项目需求，可以通过{@link #getRestOperations()}来获取更多调用方式
 * @author 陈进科
 * @since 1.0  2019年1月9日
 */
public interface RestOperationsSupport {


	/**
	 * 获取Rest操作对象，只有当完全自定义发送消息的时候使用
	 * @return
	 */
	public RestOperations getRestOperations();
}
