package com.landray.kmss.sys.simplecategory.model;

import java.util.List;

import com.landray.kmss.sys.category.model.ISysCategoryBaseModel;

/**
 * 简单分类接口
 * 
 * @author wubin
 * 
 */
public interface ISysSimpleCategoryModel extends ISysCategoryBaseModel {
	/*
	 * 可阅读者
	 */
	public List getAuthReaders();

	public void setAuthReaders(List authReaders);

	/*
	 * 可编辑者
	 */
	public List getAuthEditors();

	public void setAuthEditors(List authEditors);

	/*
	 * 其他可阅读者
	 */
	public List getAuthOtherReaders();

	public void setAuthOtherReaders(List authOtherReaders);

	/*
	 * 其他可编辑者
	 */
	public List getAuthOtherEditors();

	public void setAuthOtherEditors(List authOtherEditors);

	/*
	 * 所有可编辑者
	 */
	public List getAuthAllEditors();

	public void setAuthAllEditors(List authAllEditors);

	/*
	 * 所有可阅读者
	 */
	public List getAuthAllReaders();

	public void setAuthAllReaders(List authAllReaders);

	/*
	 * 所有人可阅读标记
	 */
	public Boolean getAuthReaderFlag();

	public void setAuthReaderFlag(Boolean authReaderFlag);
	
}
