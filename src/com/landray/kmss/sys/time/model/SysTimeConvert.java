package com.landray.kmss.sys.time.model;

@SuppressWarnings("serial")
public class SysTimeConvert {
	protected java.lang.String name;

	protected SysTimeConvertClazz clazz = new SysTimeConvertClazz();

	public java.lang.String getName() {
		return name;
	}

	public void setName(java.lang.String name) {
		this.name = name;
	}

	public SysTimeConvertClazz getClazz() {
		return clazz;
	}

	public void setClazz(SysTimeConvertClazz clazz) {
		this.clazz = clazz;
	}

	protected java.lang.String fdId;

	public java.lang.String getFdId() {
		return fdId;
	}

	public void setFdId(java.lang.String fdId) {
		this.fdId = fdId;
	}

	protected java.lang.String startDate;
	/*
	 * 结束时间
	 */
	protected java.lang.String endDate;
	/*
	 * 类型
	 */
	protected java.lang.String type;
	protected java.lang.String timeType;
	/*
	 * 颜色
	 */
	protected java.lang.String color;
	/*
	 * 开始周
	 */
	protected java.lang.String fromWeek;
	/*
	 * 结束周
	 */
	protected java.lang.String toWeek;
	/*
	 * 班次
	 */
	protected String invalid;

	protected String elementId;// 组织架构id
	protected String elementName;// 组织架构name
	protected String date;// 日期 yyyy-MM-dd 批量排班时使用

	protected String restStart;
	protected String restEnd;
	protected Float fdTotalDay;
	/**
	 * 午休结束时间类型，1 当日 2次日
	 */
	protected Integer fdRestEndType;
	/**
	 * 午休开始时间类型，1 当日 2次日
	 */
	protected Integer fdRestStartType;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getElementId() {
		return elementId;
	}

	public void setElementId(String elementId) {
		this.elementId = elementId;
	}

	public String getElementName() {
		return elementName;
	}

	public void setElementName(String elementName) {
		this.elementName = elementName;
	}

	public String getInvalid() {
		return invalid;
	}

	public void setInvalid(String invalid) {
		this.invalid = invalid;
	}


	public java.lang.String getStartDate() {
		return startDate;
	}

	public void setStartDate(java.lang.String startDate) {
		this.startDate = startDate;
	}

	public java.lang.String getEndDate() {
		return endDate;
	}

	public void setEndDate(java.lang.String endDate) {
		this.endDate = endDate;
	}

	public java.lang.String getType() {
		return type;
	}

	public void setType(java.lang.String type) {
		this.type = type;
	}

	public java.lang.String getColor() {
		return color;
	}

	public void setColor(java.lang.String color) {
		this.color = color;
	}

	public java.lang.String getFromWeek() {
		return fromWeek;
	}

	public void setFromWeek(java.lang.String fromWeek) {
		this.fromWeek = fromWeek;
	}

	public java.lang.String getToWeek() {
		return toWeek;
	}

	public void setToWeek(java.lang.String toWeek) {
		this.toWeek = toWeek;
	}



	public java.lang.String getTimeType() {
		return timeType;
	}

	public void setTimeType(java.lang.String timeType) {
		this.timeType = timeType;
	}

	public String getRestStart() {
		return restStart;
	}

	public void setRestStart(String restStart) {
		this.restStart = restStart;
	}

	public String getRestEnd() {
		return restEnd;
	}

	public void setRestEnd(String restEnd) {
		this.restEnd = restEnd;
	}

	public Float getFdTotalDay() {
		return fdTotalDay;
	}

	public void setFdTotalDay(Float fdTotalDay) {
		this.fdTotalDay = fdTotalDay;
	}
	public Integer getFdRestEndType() {
		if(fdRestEndType ==null){
			fdRestEndType =1;
		}
		return fdRestEndType;
	}

	public void setFdRestEndType(Integer fdRestEndType) {
		this.fdRestEndType = fdRestEndType;
	}

	public Integer getFdRestStartType() {
		if(fdRestStartType ==null){
			fdRestStartType =1;
		}
		return fdRestStartType;
	}

	public void setFdRestStartType(Integer fdRestStartType) {
		this.fdRestStartType = fdRestStartType;
	}
}
