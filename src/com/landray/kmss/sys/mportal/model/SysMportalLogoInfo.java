package com.landray.kmss.sys.mportal.model;

import com.landray.kmss.sys.appconfig.model.BaseAppConfig;

public class SysMportalLogoInfo extends BaseAppConfig {
	public SysMportalLogoInfo() throws Exception {
		super();
	}

	public String getLogoUrl() {
		return getValue("logoUrl");
	}

	public void setLogoUrl(String logoUrl) {
		setValue("logoUrl", logoUrl);
	}

	@Override
	public String getJSPUrl() {
		return "/sys/mportal/sys_mportal_logo/sysMportalLogo.do?method=list";
	}
}
