package com.landray.kmss.sys.filestore.event;

import org.springframework.context.ApplicationEvent;

public class ConvertFinishEvent extends ApplicationEvent {

	public ConvertFinishEvent(Object convertQueue) {
		super(convertQueue);
	}

	private boolean success;

	public boolean isSuccess() {
		return success;
	}

	public void setSuccess(boolean success) {
		this.success = success;
	}

}
