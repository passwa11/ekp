package com.landray.kmss.sys.filestore.scheduler.third.wps.center.domain;

/**
 * 转换执行对象
 */
public class ConvertGoing {
    /**
     * 任务ID
     */
    private String taskId;

    /**
     * 转换KEY
     */
    private String convertKey;

    /**
     * 文件ID
     */
    private String fileId;

    /**
     * 附件ID
     */
    private String attMainId;

    /**
     * 处理情况
     */
    private Boolean dealStatus;

    public ConvertGoing() {
    }

    public ConvertGoing(String taskId, String fileId, String attMainId, String convertKey) {
        this.taskId = taskId;
        this.fileId = fileId;
        this.attMainId = attMainId;
        this.convertKey = convertKey;

    }

    public String getTaskId() {
        return taskId;
    }

    public void setTaskId(String taskId) {
        this.taskId = taskId;
    }

    public String getConvertKey() {
        return convertKey;
    }

    public void setConvertKey(String convertKey) {
        this.convertKey = convertKey;
    }

    public String getFileId() {
        return fileId;
    }

    public void setFileId(String fileId) {
        this.fileId = fileId;
    }

    public String getAttMainId() {
        return attMainId;
    }

    public void setAttMainId(String attMainId) {
        this.attMainId = attMainId;
    }

    public Boolean getDealStatus() {
        return dealStatus;
    }

    public void setDealStatus(Boolean dealStatus) {
        this.dealStatus = dealStatus;
    }

    @Override
    public String toString() {
        return "ConvertGoing{" +
                "taskId='" + taskId + '\'' +
                ", convertKey='" + convertKey + '\'' +
                ", fileId='" + fileId + '\'' +
                ", attMainId='" + attMainId + '\'' +
                ", dealStatus='" + dealStatus + '\'' +
                '}';
    }
}
