package com.landray.kmss.sys.filestore.convert.constant;

/**
 * 线程时间常量
 */
public class ThreadTime {
    /**
     * 1000毫秒
     */
    public static long ONE_THOUSAND_MILLISECOND = 1000;

    /**
     * 一小时
     */
    public static long AN_HOUR = 60 * 60 * ONE_THOUSAND_MILLISECOND;

    /**
     * 半小时
     */
    public static long HALF_AN_HOUR = AN_HOUR / 2;
}
