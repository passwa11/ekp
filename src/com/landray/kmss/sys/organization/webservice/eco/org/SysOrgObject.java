package com.landray.kmss.sys.organization.webservice.eco.org;

/**
 * 生态组织对象
 * 
 * @author panyh
 *
 *         2020年9月17日 下午1:22:48
 */
public class SysOrgObject {
	private String id;

	private String name;

	public SysOrgObject() {
	}

	public SysOrgObject(String id) {
		this.id = id;
	}

	public SysOrgObject(String id, String name) {
		this.id = id;
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
