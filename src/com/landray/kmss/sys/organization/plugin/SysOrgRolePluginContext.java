package com.landray.kmss.sys.organization.plugin;

import com.landray.kmss.sys.organization.model.SysOrgElement;
import com.landray.kmss.sys.organization.model.SysOrgRole;

public class SysOrgRolePluginContext {
	private SysOrgElement baseElement;

	private SysOrgRole role;

	public SysOrgRolePluginContext(SysOrgElement baseElement, SysOrgRole role) {
		super();
		this.baseElement = baseElement;
		this.role = role;
	}

	public SysOrgElement getBaseElement() {
		return baseElement;
	}

	public void setBaseElement(SysOrgElement baseElement) {
		this.baseElement = baseElement;
	}

	public SysOrgRole getRole() {
		return role;
	}

	public void setRole(SysOrgRole role) {
		this.role = role;
	}
}
