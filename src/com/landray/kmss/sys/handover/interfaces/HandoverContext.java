package com.landray.kmss.sys.handover.interfaces;

import com.landray.kmss.sys.organization.model.SysOrgElement;

/**
 * 工作交接上下文
 * 
 * @author 缪贵荣
 * 
 */
public class HandoverContext {

	/**
	 * 工作交接人
	 */
	private final SysOrgElement from;

	public SysOrgElement getFrom() {
		return from;
	}

	/**
	 * 工作接收人
	 */
	private final SysOrgElement to;

	public SysOrgElement getTo() {
		return to;
	}

	public HandoverContext(SysOrgElement from, SysOrgElement to) {
		this.from = from;
		this.to = to;
	}

}
