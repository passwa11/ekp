package com.landray.kmss.sys.ui.xml.model;

public class SysUiCombin extends SysUiXmlBase {
	protected String fdFile;

	public String getFdFile() {
		return fdFile;
	}

	public void setFdFile(String fdFile) {
		this.fdFile = fdFile;
	}

	public SysUiCombin(String id, String name, String thumb, String help,
			String file) {
		this.fdId = id;
		this.fdName = name;
		this.fdThumb = thumb;
		this.fdHelp = help;
		this.fdFile = file;
	}

}
