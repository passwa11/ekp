package com.landray.kmss.sys.ui.xml.model;

public class SysUiTemplate extends SysUiXmlBase {

	private String path;
	private String thumbPath;

	public SysUiTemplate() {
	}
	public SysUiTemplate(String id, String name, String file, String thumb,
			String help, String _for, String designer, String path,
			String thumbPath) {
		this.fdId = id;
		this.fdName = name;
		this.fdFile = file;
		this.fdThumb = thumb;
		this.fdHelp = help;
		this.fdFor = _for;
		this.fdDesigner = designer;
		this.path = path;
		this.thumbPath = thumbPath;
	}
 
	protected String fdFile; 
	protected String fdFor;
	protected String fdDesigner;

	public String getFdDesigner() {
		return fdDesigner;
	}

	public void setFdDesigner(String fdDesigner) {
		this.fdDesigner = fdDesigner;
	}

	public String getFdFor() {
		return fdFor;
	}

	public void setFdFor(String fdFor) {
		this.fdFor = fdFor;
	}

	public String getFdFile() {
		return fdFile;
	}

	public void setFdFile(String fdFile) {
		this.fdFile = fdFile;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getThumbPath() {
		return thumbPath;
	}

	public void setThumbPath(String thumbPath) {
		this.thumbPath = thumbPath;
	}

}
