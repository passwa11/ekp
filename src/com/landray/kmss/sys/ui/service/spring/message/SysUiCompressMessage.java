package com.landray.kmss.sys.ui.service.spring.message;

import com.landray.kmss.sys.cluster.interfaces.message.IMessage;

/**
 *  pc端合并压缩开关事件信息
 * @desc Create with IntelliJ IDEA 
 * @author lr-linyuchao
 * @date 2021-10-20
 */
public class SysUiCompressMessage implements IMessage {
    /**
     * 是否打开压缩合并js
     */
    private boolean isOpenCompress = false;

    public SysUiCompressMessage() {
    }

    public SysUiCompressMessage(boolean isOpenCompress) {
        this.isOpenCompress = isOpenCompress;
    }

    public boolean isOpenCompress() {
        return isOpenCompress;
    }

    public void setOpenCompress(boolean isOpenCompress) {
        this.isOpenCompress = isOpenCompress;
    }
}
