public class ElecChannelSealMaterial implements IElecChannelRequestMessage {

	private static final long serialVersionUID = 1L;
	
	private String sealMaterialId;
	
	private String sealMaterialName;
	
	public ElecChannelSealMaterial() {
		super();
	}
	
	public ElecChannelSealMaterial(String sealMaterialId,
			String sealMaterialName) {
		super();
		this.sealMaterialId = sealMaterialId;
		this.sealMaterialName = sealMaterialName;
	}

	public String getSealMaterialId() {
		return sealMaterialId;
	}

	public ElecChannelSealMaterial setSealMaterialId(String sealMaterialId) {
		this.sealMaterialId = sealMaterialId;
		return this;
	}

	public String getSealMaterialName() {
		return sealMaterialName;
	}

	public ElecChannelSealMaterial setSealMaterialName(String sealMaterialName) {
		this.sealMaterialName = sealMaterialName;
		return this;
	}

	@Override
	public String toString() {
		return "ElecChannelSealMaterial [sealMaterialId=" + sealMaterialId
				+ ", sealMaterialName=" + sealMaterialName + "]";
	}
}
