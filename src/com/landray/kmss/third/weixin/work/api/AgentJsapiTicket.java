package com.landray.kmss.third.weixin.work.api;

public class AgentJsapiTicket extends JsapiTicket {

	private String agentId;
	public AgentJsapiTicket(String corpId, String agentId, String ticket, Long expireTime) {
		super(corpId,ticket,expireTime);
		this.setAgentId(agentId);
	}

	public String getAgentId() {
		return agentId;
	}

	public void setAgentId(String agentId) {
		this.agentId = agentId;
	}
}
