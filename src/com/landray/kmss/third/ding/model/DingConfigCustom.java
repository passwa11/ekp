package com.landray.kmss.third.ding.model;

public class DingConfigCustom {

	public String title;
	public String synWay;
	public String target;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getSynWay() {
		return synWay;
	}

	public void setSynWay(String synWay) {
		this.synWay = synWay;
	}

	public String getTarget() {
		return target;
	}

	public void setTarget(String target) {
		this.target = target;
	}
}
