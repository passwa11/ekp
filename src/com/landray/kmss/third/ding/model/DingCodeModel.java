package com.landray.kmss.third.ding.model;

import com.landray.kmss.common.model.BaseModel;

public class DingCodeModel extends BaseModel {
	protected String fdCode;

	public String getFdCode() {
		return fdCode;
	}

	public void setFdCode(String fdCode) {
		this.fdCode = fdCode;
	}

	protected String fdUserid;

	public String getFdUserid() {
		return fdUserid;
	}

	public void setFdUserid(String fdUserid) {
		this.fdUserid = fdUserid;
	}

	protected Long fdExpiresTime;

	public Long getFdExpiresTime() {
		return fdExpiresTime;
	}

	public void setFdExpiresTime(Long fdExpiresTime) {
		this.fdExpiresTime = fdExpiresTime;
	}

	@Override
    public Class getFormClass() {
		return DingCodeModel.class;
	}
}
