package com.landray.kmss.third.wechat.dto;

public class WeChatPostData {
	
	private String todoId;
	private String postSubject;
	private String personId;
	
	public String getPostSubject() {
		return postSubject;
	}
	public void setPostSubject(String postSubject) {
		this.postSubject = postSubject;
	}
	public String getTodoId() {
		return todoId;
	}
	public void setTodoId(String todoId) {
		this.todoId = todoId;
	}
	public String getPersonId() {
		return personId;
	}
	public void setPersonId(String personId) {
		this.personId = personId;
	}
	
	public WeChatPostData(){};
	
	public WeChatPostData(String todoId, String postSubject, String personId) {
		this.todoId = todoId;
		this.postSubject = postSubject;
		this.personId = personId;
	}
	
	@Override
    public String toString() {
		return "weChatPostData [todoId=" + todoId + ", postSubject="
				+ postSubject + ", personId=" + personId + "]";
	}
	
	
	

}
