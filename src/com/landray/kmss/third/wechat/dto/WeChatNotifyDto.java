package com.landray.kmss.third.wechat.dto;

import java.util.List;

/**
 * @author:kezm
 * @date :2014-6-17下午06:05:30
 */
public class WeChatNotifyDto {

	private String id;
	private String link;
	private String linkSubject;
	private String subject;
	private String license;
	private String mailFormat;
	private String content;
	private String fdBundle;
	private String replaceText;
	private String notifyType;

	// 发送者ID
	private String fromPersonId;

	// 发送者的邮件
	private String fromPersonEmail;

	// 发送者姓名
	private String fromPersonName;

	// 语言
	private List<String> defaultLangs;

	// 接收者id,email,mobile,name 信息
	private List<String> personList;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public String getLinkSubject() {
		return linkSubject;
	}

	public void setLinkSubject(String linkSubject) {
		this.linkSubject = linkSubject;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public String getLicense() {
		return license;
	}

	public void setLicense(String license) {
		this.license = license;
	}

	public String getFromPersonId() {
		return fromPersonId;
	}

	public void setFromPersonId(String fromPersonId) {
		this.fromPersonId = fromPersonId;
	}

	public String getFromPersonName() {
		return fromPersonName;
	}

	public void setFromPersonName(String fromPersonName) {
		this.fromPersonName = fromPersonName;
	}

	public List<String> getPersonList() {
		return personList;
	}

	public void setPersonList(List<String> personList) {
		this.personList = personList;
	}

	public List<String> getDefaultLangs() {
		return defaultLangs;
	}

	public void setDefaultLangs(List<String> defaultLangs) {
		this.defaultLangs = defaultLangs;
	}

	public String getMailFormat() {
		return mailFormat;
	}

	public void setMailFormat(String mailFormat) {
		this.mailFormat = mailFormat;
	}

	public String getFromPersonEmail() {
		return fromPersonEmail;
	}

	public void setFromPersonEmail(String fromPersonEmail) {
		this.fromPersonEmail = fromPersonEmail;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getFdBundle() {
		return fdBundle;
	}

	public void setFdBundle(String fdBundle) {
		this.fdBundle = fdBundle;
	}

	public String getReplaceText() {
		return replaceText;
	}

	public void setReplaceText(String replaceText) {
		this.replaceText = replaceText;
	}

	/**
	 * @return notifyType
	 */
	public String getNotifyType() {
		return notifyType;
	}

	/**
	 * @param notifyType
	 *            要设置的 notifyType
	 */
	public void setNotifyType(String notifyType) {
		this.notifyType = notifyType;
	}
}
